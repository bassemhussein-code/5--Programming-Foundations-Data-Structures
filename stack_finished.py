# try out the Python stack functions

# create a new empty stack
stack = []               # in real .. this is an empty list . 

# push items onto the stack
stack.append(1)                         # list's method (append) 
stack.append(2)
stack.append(3)
stack.append(4)

# print the stack contents
print(stack)

# pop an item off the stack
x = stack.pop()                         # list's method (pop)
print(x)
print(stack)
